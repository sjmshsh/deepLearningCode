# 2. 用reshape转为二维（1个样本 × 3个类别）
y_2d = y_1d.reshape(1, 3)  # 第一个维度=1（样本数），第二个维度=3（类别数）
t_2d = t_1d.reshape(1, 3)

print("\n转换后y维度：", y_2d.ndim)  # 输出：2（2维）
